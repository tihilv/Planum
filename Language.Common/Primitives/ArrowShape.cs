namespace Language.Common.Primitives;

public enum ArrowShape: byte
{
    No = 0,
    Arrow = 1, // >
    Triangle = 2, // ^ , |>
    DiamondFull = 3, // *
    DiamondEmpty = 4, // o
    Cross = 5, // +
    Rectangle = 6, // #
    LittleTriangle = 7, // >>
    Circle = 8, // 0
    Connect = 9 // (0
}